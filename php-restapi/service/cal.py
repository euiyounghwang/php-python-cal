
class cal(object):
    ''' class for simple add/minus/multiply/divide '''    
    
    def __init__(self):
        pass
    
    def plus(self, arg1, arg2):
        return int(arg1) + int(arg2)
    
    def minus(self, arg1, arg2):
        return int(arg1) - int(arg2)
    
    def multiply(self, arg1, arg2):
        return int(arg1) * int(arg2)
    
    def divide(self, arg1, arg2):
        return int(arg1) / int(arg2)
